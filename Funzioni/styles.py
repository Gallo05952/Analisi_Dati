from tkinter import ttk

class Styles:

    def __init__(self):
        pass

    def create_styles():
        style = ttk.Style()
        style.configure("Style1.TButton", foreground="green", background="white")

        style.configure("Style2.TCheckbutton", foreground="blue", background="green")

        style.configure("Style3.TCheckbutton2", foreground="red", background="yellow")

        style.configure("Style4.TCheckbutton3", foreground="black", background="white")


